                    <center><br /><b>Animals Who Heal</b><br /><i>Charlane Pereira</i><br /></center>
                    <p align="center"><img border="0" src="/images/goa-streets/rohini-radhika.jpg" alt="Rohini Fernandes and Radhika Nair" align="center" hspace="0" width="540" height="284" /></p>
                    <p align="justify">You are exhausted after a hard day's work, and speed home to find your four-legged furry canine bounding happily to welcome you. It doesn't require any special insight to feel the warmth of such unconditional love - and to understand that it does bring about a certain peace.</p>
                    <p align="justify">But does owning a pet really have any health benefits? There is consensus in the scientific community that the relationship we humans have with companion animals does bring favorable results, even if there is a need for more scientific data to verify many claims. Proponents of animal-assisted therapy, or AAT, say it decreases stress, lowers blood pressure, steadies rapid breathing, eases pain, lowers cholesterol, improves moods, helps people to socialize, prevents strokes, eases allergies and helps boost immunity, in addition to helping children to develop.</p>
                    <p align="justify">Sheela, an autistic child, found it very difficult to interact with people and had a short attention span. After ten sessions with a therapy dog named Anna in Mumbai, the eight-year-old would run straight towards her furry friend. She began looking Anna in the eye while interacting with her - something she never did with people - and realized that if she throws the ball in the right direction, Anna would catch it. Her attention span and concentration improved. And she started saying simple words like 'ball', 'dog' and 'come'.</p>
                    <p align="justify">A 1999 Canadian study found that pet owners were more 'socially engaged' than non-pet owners. An Austrian study found that pet ownership led to an increase in social contact (such as neighbors chatting as they walk their dogs). The concept of pet therapy has evolved over the years, backed by many scientific studies. However, critics contend the therapy is more of a temporary rather than long-term fix, and question the quality of many of the studies. In particular, there's been quite a bit of questioning of the use of dolphins to alleviate human ailments (mostly by people who dislike keeping dolphins captive for such a purpose).</p>
                    <p align="justify">Pet therapy uses dogs or other animals to help people cope better with various health problems.  Though dogs are a popular choice of therapeutic animals, cats, horses, dolphins, rabbits, fish and birds have also been used.</p>
                    <p align="justify">Sigmund Freud kept quite a few dogs and often had his pet chow Jofi in the room during his groundbreaking sessions of psychoanalysis. He noted that patients responded better with the presence of the dog, gaining reassurance at the understanding that the animal could never be shocked or bothered by anything they could say.</p>
                    <p align="justify">Several theories have been postulated as to why we respond positively to animals. One popular one called biophilia holds that humans' evolutionary survival often depended on animals signaling either danger or safety - and that's why today seeing animals in a peaceful state can stir feelings of security and well-being in humans, which in turn aids in the process of healing.</a></p>
                    <p align="justify">Pet therapy has yet to take root in Goa, to be sure.</p>
                    <p align="justify">Explains veterinarian Dr. Niceta Cunha Costa, "Pet therapy is gaining a lot of importance nowadays, but I do not know of any places or institutions offering such aid here in Goa. I do know of a horse owner having dreams of helping out such people. She believes that the horse reads the mind of the person and slowly brings the individual back on track."</p>
                    <p align="justify">India does have some organizations that use animals to aid in recovery of patients. Animal Angels Foundation is a Mumbai-based outfit founded by Rohini Fernandes and Radhika Nair, clinical psychologists, certified practitioners of animal-assisted therapy and professional dog trainers.</p>
                    <p align="justify">"Angel (a dog), our co-therapist, provides that extra something that a human therapist cannot always give during therapy. Angel's presence serves as an effective ice-breaker with withdrawn and uncooperative clients who see the therapist as more friendly if there is a therapy pet present in the room," the two women said in a joint response to some emailed questions.</p>
                    <p align="justify">"Having a friendly pet in the room allows the client to relax, which helps build rapport and trust with the therapist. Having a warm, furry dog to stroke and hug is also therapeutic to clients who are not comfortable being touched by people, especially for those who have been physically or sexually abused.</p>
                    <p align="justify">"We work with dogs. What is necessary is a dog with a friendly personality and very good temperament.  We have Labradors, Great Danes, cocker spaniels and even mixed breeds as part of our team."</p>
                    <p align="justify">Rohini and Radhika have many successes to report, including a young man in a wheelchair named Vinod. His hands are very weak and he does not use his right arm at all. Someone suggested pet therapy. In the beginning Vinod would ask the therapists what was the use of playing with Angel. He would throw the ball for her, play a game of tug of war, pet her, brush her, feed her treats and give her commands like "sit", "down" and "come". He refused to do any of these activities with his right arm.</p>
                    <p align="justify">After a couple of sessions, he reported that his left arm had become stronger and he could hold the tug toy longer. He then asked the therapist if he could pet Angel with his right arm because if his left arm had strengthened, then maybe the same would happen with his right. And then he did it, gliding his right hand over Angel's fur.</p>
