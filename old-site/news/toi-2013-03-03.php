                          	<p align="justify"><b><u>Jiyon's mother's experience in her own words</u></b></p>
                          	<p align="justify">I am hoping that from now on, travelling with Jiyon and Simbaa will not be an ordeal I would
need to worry about, it would be a trip that each time I would finally look forward to. I am so
sure and hopeful that all parents of kids with similar behavioural issues as that of Jiyon will
now feel relieved, while travelling with their therapy dogs.</p>
							<p align="justify">It'll be a far better option to make a travel plan by flight compared to a train - cost wise, adult fare in 1st AC is close to a flight ticket and if you book in advance the difference will be even lower. Therapy animal allowed in the cabin is not charged for by the Airline. Indian Railways' hygiene parameters did intrigue me. Whilst toilets in 1st AC continued to remain dirty and berths continued to be dusty, Railways took every care to ensure eradication of rats from bogies of Indian railways. To achieve zero rat tolerance, they put rat poison under every seat without fail; no dustbin placed but rat poison a must in all coupes. At 2 in the morning my Simbaa in his zest for investigation, went under a seat and tasted the rat poison.
Hell broke loose on us. Seldom have I been so helpless and hapless looking at Simbaa vomiting and in pain. He suffered for a whole day and half of night before he could be reached to a vet.</p>
							<p align="justify">I then decided trains would be a strict No NO.. I decided to speak to Jet Airways and ask for help. Help in understanding Jiyon's needs while travelling. With Simbaa around he is happy calm and at times more composed than me when i am travelling :)... I thought, in case the airline is unable to allow Simbaa in the cabin with us we will take him on cargo hold. There was no fight no argument in the process of applying for this allowance, and I must not fail to mention that at Jet airways I came across understanding, non-stuck up, non-judgmental people who worked as a team to make it work for us.</p>
							<p align="justify">I scrolled down to the end of the page of Jet Airways website that explains the special assistance offered to passengers as and when needed. It only spoke about Service Dogs being of 'service' to sight impaired mostly and at the most aiding someone with hearing impairment. I also read up other websites which said that only service dogs/help dogs are allowed. Depressed people are not allowed dogs for 'feel good' issues in the cabin. I called reservations to find out more.</p>
							<p align="justify">They clearly mentioned that hearing and visual impairments are offered aid, and assistance is limited only to such cases. I tried asking about other issues and then figured out that not much would work out from the discussion. They asked me to speak to the medical team in Kolkata and so I did. Kolkata team had no answer so I spoke to Mumbai the airlines'HO, at Kolkata medical team's advice. The doctors clearly mentioned this has never happened, such a request too had come for the first time. They, I guess needed seniors to discuss this with, and lot many departments were involved.</p>
							<p align="justify">I wrote to the medical team requesting them to consider a mental health issue at par with a physical health issue, to offer the same help, compassion, and understanding offered to passengers with physical challenge, to let Simbaa travel with Jiyon in the cabin and with us.
Confusion reigned for quite some time amongst customer service and medical team. However, the airline kept calling me, asking questions, seeking a certificate of the doctor and a therapy dog's relevance to Jiyon s mental state while traveling etc.; not for once did they say, that they would not consider. They kept dwelling on it. Finally, a day before my travel date the airline agreed that Simbaa would travel with his master in the cabin.</p>
							<p align="justify">I must tell you here that although the airline has taken a huge step forward by this allowance, such a gesture should never be misused. Your dog should definitely behave as a therapy dog should. Simbaa doesn't bark, is completely toilet trained, listens to commands of sitting, lying down, staying at a place, being within a particular periphery if asked and till the time he is released, and all the other basics. He is a thorough young gentle well behaved boy, shaping up I'm sure, to be a thorough gentleman. One should not ask for allowance of a pet that does not adhere to basic expected behaviours. One must remember that the airline is responsible for comfort and security of so many other passengers too and not just one with the pet. Let's use this allowance, for a genuine cause and not to flaunt rules. Please do train your pet. It is an investment of a lifetime.</p>
