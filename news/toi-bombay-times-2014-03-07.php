                           <center><br /><b>Healing with animal-assisted therapy</b><br /><i>Reza Noorani</i><br /></center><br /><br />
                           <p align="center"><i>With a Canadian university using puppies as stress-busters for students, we see more animals helping people with different ailments</i></p>
                           <p align="center"><img border="0" src="/images/toi/bombay-times/2014-03-07/animals-help-a-child-unleash-its-creative-potential.JPG" alt="Animals help a child unleash its creative potential" align="center" hspace="0" width="536" height="287" /></p>
                           <p align="justify">City-based nine-year-old kid, Anjali, a girl with special needs, was usually a calm child. She had suffered mentally because when she was young, she saw her father commit suicide in front of her eyes and was seeking therapy for the trauma. However, one day, in the middle of therapy, she just stopped talking.</p>
                           <p align="justify">She turned aggressive, started hitting her classmates and her overall behaviour became abusive. Therapist Radhika Nair who was treating her, says, "Anjali would not talk to anyone. It was strange because we were already treating her for the trauma she suffered seeing her father pass away, but instead of recovering, she became quiet.</p>
                           <p align="justify">In the course of her therapy, it was revealed that Anjali's maternal uncle was molesting her at the time." This she didn't reveal to a therapist, but to a therapy dog who was working with her, adds Nair. "The child wanted to express her emotions, but couldn't do it to another human being as she had lost trust in humans, but she confided to the dog." This revelation not only helped treat Anjali, but also put a stop to a serious form of abuse. It brought a new form of therapy to light.</p><br />
                           <p align="justify"><b><font color="red">ANIMALS AS THERAPISTS</font></b></p>
                           <p align="center"><img border="0" src="/images/toi/bombay-times/2014-03-07/joint-playtime-with-a-pet.JPG" alt="Joint playtime with a pet" align="center" hspace="10" width="436" height="258" /></p>
                           <p align="justify">This is a case where animal-assisted therapy (AAT) comes into play. AAT is a form of treatment where animals are used to motivate the patient and act as mediators when the patient doesn't respond to normal therapy. AAT has a surprising success rate since people, mostly kids, respond amazingly to pets. "When we walk in with dogs, the client looks at us in a positive way. Then we are not therapists or doctors, but fun people coming with a dog", adds Nair.</p><br />
                           <p align="justify"><b><font color="red">TREATING AUTISM</font></b></p>
                           <p align="justify">While pets are used to treat post traumatic stress disorder to heal people who have been through lifealtering incidents like terrorist attacks, bomb blasts and even soldiers back from war zones, they work best with kids who are autistic. "We found our most positive results with children who have autism. Since it hinders a kid's ability to interact with people and they live in a world of their own, a dog is someone who is non-judgemental and kids open up to them more easily. Another example Nair cites is of a kid with severe autism who avoided eye contact with people,
including her parents, "This child didn't respond to her name, she avoided eye contact and remained very aloof. After a couple of tough sessions she opened up, and the very next day, after a fruitful session where she was playing with the therapy dog, she actually held the hand of a child in class", she says.</p><br />
                           <p align="justify"><b><font color="red">DOGS ARE COMMON, BUT CATS CAN BE OF HELP TOO</font></b></p>
                           <p align="justify">The most common therapy animals are dogs (they even use a Golden Retriever, a Beagle and pug), although cats too are used. Says animal therapist Rohini Fernandes, "We use cats with adults. They go and curl up in their laps, which helps them relieve stress as they pet the cat which is quite soothing and reveal things to us which they otherwise wouldn't." While with kids, it is dogs who work best, as kids often want to run around and play, mid-therapy. "The dogs that are used to treat kids are trained. We select extremely well-behaved dogs with a good temper," says Fernandez who trains dogs to not react even if the child hurts it.</p>
