		                    <center><br /><b>Pets, Your Best Bet!</b><br /><i>Naressa Coutinho</i><br /></center>
                            <p align="justify"><b>Ancient myths speak of dogs as doorkeepers of the dead or traveler's guardians, having supernatural abilities and you might have even heard from elders of howling dogs as bad omens. So, they've always been part of our mythemes, sometimes liked and sometimes feared. But ever present! But, they also have another talent. They can heal. Wouldn't have guessed it would you? Naressa Coutinho introduces you to the wonderful world of Animal assisted therapy.</b></p>
                            <p align="center"><img border="0" src="/images/afternoon/2010-11-24/afternoon-2010-11-24-pets-your-best-bet-01.jpg" alt="Co therapist Angel" align="left" hspace="0" width="540" height="323" /></p>
                            <p align="justify">&nbsp;</p>
		                    <p align="justify">Fact of the matter, proven all over the world is that dogs have therapeutic powers, in fact not only dogs but cats, farm animals and all our furry friends. Did you know, by only caressing an animal you could lower your blood pressure? Our Hi-tech consumerist society has either forgotten or neglected the effectiveness of the simple remedies available in nature. A pet dog may work far better than your prescription drugs.</p>
                            <p align="justify">Animal-assisted-therapy is an effective program used by medical, mental or physical health professionals and psychologists to help them avail patients who immediately open up at the mere presence of a pet dog or cat. For instance in the case of autistic children who are uncomfortable mingling socially, show drastic improvements around animals. Caring for pets encourages them to be responsible and focused. Animals serve as a substitute for absence of children, loneliness depression and helps provide order to people's lives.</p>
                            <p align="justify">Rohini Fernandes, an animal therapist specialised in clinical psychology and the co-founder of the Animal Angels foundation, says that one needs to have a qualification in the fields of mental or physical health. It has to be in conjunction with this because animal therapy is not a specialised field but an interdisciplinary one that you could pursue. "It's a complementary therapy used by any therapist," says Rohini. Hence you could be a psychiatrist or speech therapist or an occupational therapist and use a therapy pet as a motivator to help you help connect with your patients. It could be used for play behavior whilst dealing with developmental and emotional problems like autism, neurological disorders, cerebral syndromes in children and schizophrenia or bipolar disorders in adults, to help them improve their social, physical and cognitive skills.</p>
                            <p align="justify">One may decide to be an individual animal therapist or care to associate themselves with institutions, like special schools, orphanages, old age homes and medical health centers. However, what is essential for an animal therapist is to posses the knowledge that would qualify the use or disuse of an animal that would hinder the physiological treatment due to the possibility of dependence of the patient on the animal. India still doesn't have the vocational and educational backing required to train animal therapists, which means that one has to acquire this knowledge overseas. Rohini's efforts to use this therapy in hospitals have gone in vain at the feet of hesitant doctors and administrations. The course equips the therapist with much needed knowledge of the use of dog vaccination and proper methods that would dismiss the fear of infection. In fact, from a recovery perspective, therapy pets have been viewed as successful stress busters all over the world. Thus, one must be prepared for obstacles created by people and institutions in a country that is shut to the idea.</p>
                            <p align="center"><img border="0" src="/images/afternoon/2010-11-24/afternoon-2010-11-24-pets-your-best-bet-02.jpg" alt="Rohini Fernandes using Animal Assisted Therapy to work with a patient" align="left" hspace="0" width="540" height="427" /></p>
                            <p align="justify">&nbsp;</p>
                            <p align="justify">"Basically you need to love animals, understand them, train them into good behavior that is essential to this profession," states Rohini who also trains animals into obedient behavior after a selection process and criteria that the animals need to pass. Thus, one can easily see the wide scope of this career. Moreover, being fairly new a concept in India, one could look at the prospects of being a consultant, lecturer, writer or a dog trainer besides being simply a mental or physical therapist. When asked if the job of an animal therapist is stressful Rohini response is quick, "It is, in fact stress free, as you are dealing with animals who are stress busters, moreover your love for animals is combined with your love for your profession and passion."</p>
                            <p align="justify">It's fairly obvious why the earnings wouldn't see a higher range in a country like India, for now-blame it on the hopeless lack of exposure and knowledge of this career perspective as being lucrative. However, once it has established itself there's no saying how profitable it could be. With time, experience and repute, one can ensure a good bunch of clientele.</p>
                            <p align="justify">"I have 20 trained dogs that are family pets and it's a challenge if you don't have a vehicle since I first pick the dogs, then go to the centre then drop the dog back home. Indian public transport is not particularly animal friendly but it's all worth it if you love what you do," says Rohini, who finds this to be the only major challenge in her profession. It doesn't require any huge investments; a backpack of psychological groundwork and animal professionalism and you're good to go.</p>
