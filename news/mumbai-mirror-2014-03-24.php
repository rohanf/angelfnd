		                    <center><br /><b>Pet Puja: A friend in deed</b><br /><i>Mitali Parekh</i><br /><br /><br /></center>
		                    <p align="justify">Mr M doesn't like to walk, but it's important that he take a few steps. He'll do it for Buddy. Ms F thinks she's volunteering at the day centre and isn't one of them. She likes gossiping to Buddy about others, and sneaks him her tea biscuits. Buddy's handlers egg Ms F to tell him all her friends' names to keep her memory from slipping away. Mrs V is losing her speech and motor skills. She has asong for Buddy, or Buddia as she insists on calling him, every time they meet.</p>
                            <p align="justify"><img border="0" src="/images/mumbai-mirror/Buddy-at-Dignity-Dementia-Day-Care-Centre-Dadar.jpg" alt="Therapy Dog Buddy at Dignity Dementia Day Care Centre in Dadar, Mumbai" align="centre" hspace="10" width="520" height="453"></p>
							<p align="justify">Buddy is a therapy dog whose job is to delay the progression of dementia in the people who come to the Dignity Dementia Day Care Centre in Dadar. He volunteers with the Animal Angels Foundation and goes there once a week with clinical psychologists and animal assisted therapy practitioners Rohini Fernandes and Radhika Nair. "Since many of these seniors live alone or with care-takers, they get excited when they see a new face," says Rohini.</p>
                            <p align="justify">"Many people become loners or introverted as the disease progresses," says Jogeshwari Sawant, the centre's co-ordinator. "We seek engaging activities and have had pet therapists since 2006." Buddy's job is to bring them out of their shell. He is the bait for their wellbeing "Buddy needs to drink water. Will you take him for a walk?" "Do you remember his name?" "He had a biscuit, don't you remember." "See how untidy Buddy is. Will you brush him?" and so on.</p>
                            <p align="justify">The reward for his congeniality is a dabba of chopped carrots. It was his insistence on greeting people with a toy or a pillow the minute they walked through the door that caught parent Dr Bindu Sthalekar's attention. "He loves people and craves interaction," says the dermatologist.</p>
                            <p align="justify">A conscious pet parent, she was worried about the quality of life she was giving Buddy. "Me and my son work all day and I worried Buddy was sitting home alone getting bored," says the Prabhadevi resident. "I was talking to his vet about it and she told me about therapy pets." A temperament test showed that Buddy has high Emotional Quotient and relaxed in stressful conditions. Additional training honed his talent for being a ray of sunshine in the darkest areas.</p>
                            <p align="justify">The five-year-old started volunteering two years ago, and hasn't been happier. He also helps children get over their fear of dogs. "In August-July last year," says Sthalekar, "he had a ligament tear and was forced to rest for six months. You could see he was depressed and bored."</p>
                            <p align="justify">For dogs, any sort of break from the routine of eat and walk on leash, adds greatly to their quality of life. "I was more than pleased that he volunteers. We believe in seva and are glad Buddy is giving back to society," says Sthalekar. Buddy probably thinks he's living up to his name.</p>
