                    <center><br /><b>Therapists with a difference</b><br /><i>Priya Fonseca</i><br /></center>
                    <p align="center"><img border="0" src="/images/mycity4kids/priya_fonseca.jpg" alt="Priya Fonseca's profile" align="center" hspace="0" width="540" height="122" /></p>
                    <p align="center"><img border="0" src="/images/mycity4kids/header.jpg" alt="article header" align="center" hspace="0" width="540" height="237" /></p>
                    <p align="justify">Meet Angel, a therapist at the Animal Angels Foundation. No it's not a joke. Angel is a playful, friendly Golden Retriever who works with her co-therapists to help make people smile and also make them feel loved.</p>
                    <p align="center"><img border="0" src="/images/mycity4kids/angel.jpg" alt="Angel at Animal Angels Foundation" align="center" hspace="0" width="540" height="720" /></p>
                    <p align="justify">Founded by clinical psychologist and certified practitioner of Animal Assisted Therapy, Rohini Fernandes, the Animal Angels Foundation uses animals to help people. Using her knowledge of dogs along with her training in the field of psychology, she conducted research that showed that animals can bring about positive changes in a person's life. The Foundation started in 2005 after her dog, Angel completed her training as a therapy dog.</p>
                    <p align="center"><img border="0" src="/images/mycity4kids/aat.jpg" alt="article header" align="center" hspace="0" width="540" height="405" /></p>
                    <p align="justify">So what is Animal Assisted Therapy or AAT? Trained therapy animals work with a medical service professional to help or motivate a child or an adult learn or improve skills. These could be social, verbal, physical or cognitive skills. AAT also works well in tandem with the regular treatment process with individuals who have emotional or behavioural problems. Every client at the Animal Angels Foundation goes through a pre therapy, mid therapy and post therapy evaluation with all sessions being documented. This example from their website explains how the therapy has worked for a child with Autism:</p>
                    <p align="center"><img border="0" src="/images/mycity4kids/angel_with_kids.jpg" alt="Angel hanging out with kids" align="center" hspace="0" width="540" height="814" /></p>
                    <p align="justify">"When Sheela (name changed to protect identity), an eight-year-old child with Autism enters the therapy room, she runs straight towards her friend, therapy dog Casper. She then lies down by his side and rests her head on his back. Sheela found it very difficult to interact with people and had a very short attention span. She was then introduced to Casper and after just twelve sessions of animal-assisted therapy, she plays with her peers and responds positively to her teacher who says that she is 'now a happier child'. Her attention span and concentration have improved tremendously through activities with Casper like playing ball with him or walking him. She has also started saying simple words like 'ball', 'dog', 'come' etc. these improvements are seen not only in the therapy room but also in her class and home environment."</p>
                    <p align="justify">The therapy pets are chosen carefully after studying their temperament, personality, fitness and ability to be trained.  It's not just dogs but cats and fish too that may be used in therapy based on their specific skills and abilities that can assist the therapeutic process. Therapy animals differ from service animals that would typically be guide dogs for the blind or hearing dogs or mobility assistance dogs. Service animals are trained to perform tasks for the benefit of a person with a disability while therapy animals provide people contact with animals but are not limited to working with people who have disabilities.</p>
                    <p align="center"><img border="0" src="/images/mycity4kids/aat_with_children.jpg" alt="Animal Assisted Therapy with children" align="center" hspace="0" width="540" height="405" /></p>
                    <p align="justify">The Animal Angels Foundation in addition to offering AAT to children with learning disabilities and behavioural problems also has a special program for kindergarten and schools focused on educating children about animals. Through the Bow Wow workshops children understand how we can be responsible for the care of an animal and in the process also imbibe valuable life lessons on discipline, kindness, attentiveness and patience. The workshops are conducted at venues across the city on weekends and school holidays. If you want to be kept informed about the workshops, you need to register online: <a href="http://www.animalangelsfoundation.com/workshops/bow-wow.php">http://www.animalangelsfoundation.com/workshops/bow-wow.php</a></p>
                    <p align="justify">This workshop is also an interesting element that you could add to your child's birthday party. The animal and handler will interact with the children for about half an hour with petting, grooming, skills and communication being the highlights. For more information call: Rohini Fernandes: +91 98207 88703 or Radhika Nair: +91 98201 44621 or email: <a href="mailto:AnimalAngelsIndia@gmail.com">AnimalAngelsIndia@gmail.com</a></p>
