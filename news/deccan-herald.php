                    <center><br /><b>Animals are now trusted healers</b><br /><i>Baishali Adak</i><br/><br/>Four-footed friends<br /></center>
                    <p align="justify"><b>The fact that pets can provide good company is well known, but did you know that pet animals could even heal you of various mental ailments?</b></p>
                    <p align="justify"><img border="0" src="/images/deccan-herald-pic.jpg" alt="Pet therapy" align="left" hspace="10" width="250" height="172">It is being increasingly realised that pet animals like dogs, cats, rabbits, horses and even aquarium based fishes and dolphins can bring about a positive change in one's mental condition. Playing with pets can bring a person's heart beat rate and blood pressure down considerably. </p>
                    <p align="justify">In fact, autism afflicted kids worldwide are being helped by these pets, predominantly dogs, to cope up with daily challenges and lead better lives. Well, it's not for no reason that dogs are called man's best friend, and even more benefits of this friendship are only being discovered now.</p>
                    <p align="justify">Animal assisted therapy (AAT) has been practiced in Western countries for a long time, but it is only very recently that a handful of organisations in India have started providing trained animals for this purpose.</p>
                    <p align="justify">They are being introduced in the therapeutic regimens of schools, orphanages, hospitals, rehabilitation centres, prisons, mental institutions and old age homes where their help is needed.</p>
                    <p align="justify">The pioneering organisation in this field in India - Animal Angels Foundation, started in Mumbai but is now in the process of opening its centres all over the country. The founders - psychologist duo Rohini Fernandes and Radhika Nair say, "We started in 2005 with just our Golden Retriever - Angel, after whom we named this organisation.</p>
                    <p align="justify">Today we have a total 20 dogs and a cat which recently retired. Special schools form our main clients and we have seen notable improvements in the functioning of the special kids with the help of our animal angels.</p>
                    <p align="justify">For example, autistic kids can gain more attention span, build social skills and improved motivation and self-esteem in the company of trained dogs. Cats are good for senior citizens as they like to sit and talk."</p>
                    <p align="justify">She adds, "Children of all ages and nature can benefit from the presence of an aquarium with fishes. Hypersensitive kids calm down by just staring at the slow movement of the fishes and older kids can learn to be responsible by maintaining the aquarium and caring for the fishes daily."</p>
                    <p align="justify">Other than Animal Angels, another organisation Healing Horses is working towards providing horse therapy. Pushpa Bopaiah, its founder, was initially working with special kids in Bangalore.</p>
                    <p align="justify">She then moved to US to pursue a course in Animal Assisted Therapy especially. She came back to start Healing Horses in the year 2000. She says, "Initially, no one believed that horses could help a disabled child. So I had to fund Healing Horses all by myself.</p>
                    <p align="justify">In the past 12 years, however, as people started to have faith in this, Army came forward to provide me horses. Recently, I even visited the Army Wives Welfare Association in Delhi and oriented parents and psychotherapists in AAT and they are likely to start it there very soon."</p>
                    <p align="justify">Describing how horse therapy works, she says, "The gait of a horse is very similar to humans. Hence when a disabled child is riding a horse, he or she learns motor skills, gains a sense of balance, improves blood circulation and muscle strength.</p>
                    <p align="justify">At the same time if you encourage him with activities like playing with the horse and feeding it, they gain immense self-confidence."</p>
                    <p align="justify">She adds, "The horses I have for the past many years now are very sensitive. They can identify each and every movement of a child. </p>
                    <p align="justify">They even look back to check if the child is comfortable. Animals, by nature, are caring. If If cared for too, they can help humans in many ways."</p>
