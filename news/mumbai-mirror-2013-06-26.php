		                    <center><br /><b>Pawsitive Action</b><br /><i>Ankit Ajmera</i><br /><br /><br /></center>
                            <p align="justify"><img border="0" src="/images/mumbai-mirror/jiyon-gives-simbaa-a-head-massage.jpg" alt="Jiyon gives Simbaa a head massage" align="left" hspace="10" width="315" height="261"><i>Here's how one-year-old labrador Simbaa helps eight-yearold Jiyon Ganguly cope with autism.</i><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /></p>
		                    <p align="justify">Eleven months after he was born, Jiyon Ganguly's mother Parama Bhattacharya was overjoyed to see him stand up and walk by himself at an age most kids do. That he hadn't picked up lingual skills didn't worry Bhattacharya initially. The 38-year old single mother enrolled three year-old Jiyon at a playschool in Kandivili (E), close to their home.</p>
							<p align="justify">The feedback from Jiyon's teachers, however, was not encouraging. Bhattacharya, a media professional, was told that her son found it difficult to pay attention to what was going on in class. He'd avoid eye contact with playmates, mutter instead of talk and remain caught up in his own thoughts.</p>
                            <p align="justify">That the other kids laughed when Jiyon got scared of loud noises - the school bell - didn't help. Within months, the playgroup's principal called Bhattacharya and suggested Jiyon be examined by a doctor.</p>
                            <p align="justify"><b>Struck by Autism</b></p>
                            <p align="justify">A behavioural expert from the Andheri based organisation Drishti Centre diagnosed Jiyon with autism. A second opinion by Dr Vibha Krishnamurthy, founder and medical director of Ummeed, a non-profit in Delhi set up to help children with developmental disabilities, confirmed autism.</p>
                            <p align="justify">Autism is a complex neurodevelopmental disorder that manifests in children by the age of three. It affects communication, socialisation, cognition, imagination and intuitive thought, and severely inhibits a child's repertoire of interests and social interaction. The degree varies from mild to severe and is, thus, known as Autism Spectrum Disorder. At the lower end it is known as Classic Autism and at the upper end it is called Asperger Syndrome. Jiyon was diagnosed with High-Functioning Autism, in which the patient's motor skills remain fine but language skills are inhibited. Jiyon could walk, run and cycle.</p>
                            <p align="justify">He disliked being smirked at or being told 'NO'. He would have trouble understanding what another person felt. Peers and teachers said he would often walk out of conversations. Bhattacharya says, "Jiyon would get aggressive when he didn't get what he wanted. If children were playing at the swing, he wouldn't know how to wait for his turn. He would get angry and would get into trouble."</p>
                            <p align="justify"><b>Discovering hope</b></p>
                            <p align="justify">With the extended family living in Kolkata, there was almost no one who could help Bhattacharya take care of Jiyon. Few wanted to make an effort. She shifted him to a school for the differently-abled where he received the focussed attention of one teacher.</p>
                            <p align="justify">There were days when he would run about their home breaking things, sulk, play in front of the mirror, wet the floor without communicating his need to urinate, and mutter through the day.</p>
                            <p align="justify">Bhattacharya had a five-day work week, which would extend to six days on most. She'd spend most of it worrying about how her son was faring at home. "I'd sit up nights and weep. I didn't know how to cope," she admits. Often, she'd clam up, leaving him miserable and aggressive. Medication and therapy did not lead to remarkable improvement. In July 2012, Bhattacharya decided to take a sabbatical from work to find a long-term solution.</p>
                            <p align="justify">Bhattacharya did her research. "In the West, therapy dogs have been found to be highly useful for children with such conditions. I wanted to give it a try."</p>
                            <p align="justify">Consultant paediatric neurologist at Kokilaben Hospital, Dr Pradnya Gadgil, says, "Animal therapy can be beneficial for children with autism. However, it cannot be the only form of therapy employed. They also need occupational, behavioural and speech therapy simultaneously."</p>
                            <p align="justify">Last June, Bhattacharya bought a four week-old labrador pup (who they named Simbaa), and spent Rs 50,000 on getting it trained in assistance therapy. "Through research I found that, compared to other breeds, labradors are genetically predisposed to being happy. Simbaa comes from a champion blood line. His parents were frequent participants at dog shows", says Bhattacharya.</p>
                            <p align="justify"><b>Assistance training</b><br /><br /><img border="0" src="/images/mumbai-mirror/rohini-simbaa-radhika.jpg" alt="Rohini, Simbaa and Radhika" align="right" hspace="10" width="492" height="642"></p>
                            <p align="justify"><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br />Bhattacharya enrolled Simbaa at the Andheri-based Animal Angels Foundation run by Rohini Fernandes (left, in picture above) and Radhika Nair. Since 2005, the two have trained 75 dogs in assisting patients with various mental and psychological ailments.</p>
                            <p align="justify">First, Simbaa was put through a series of tests. Fernandes and Nair are clinical psychologists and certified practitioners of animal assisted therapy from the University of North Texas in the US. They have stringent criteria for selection of animals.</p>
                            <p align="justify">When approached by clients, Fernandes helps them select an animal with a suitable temperament. "A pet can be trained in assistance therapy right at birth. However, once they are grown up, few have the temperament suited for training", says Fernandes, who has designed 15 parameters for pet selection.</p>
                            <p align="justify">It is judged on its reaction to hugging, loud noises, touch, having something taken from its mouth by a stranger, its affection towards strangers, reaction to being petted by strangers and its ability to follow commands.</p>
                            <p align="justify">The friendlier it is, the better a therapy animal it can be. Naughty pets are not considered good therapy animals since they can turn dominating. "On an average, we reject 30 animals for every one we find suited for therapy. A single growl or bark during tests is enough for disqualification," says Fernandes.</p>
                            <p align="justify">Animal therapy also works for conditions like dementia, Parkinson's, ADHD and depression. Fernandes once even trained a client's pet cat to assist in coping with schizophrenia.</p>
                            <p align="justify">Bhattacharya was lucky that Simbaa met all parameters. From May to August, twice a week, Bhattacharya took Simbaa to Fernandes' workshop. Simbaa was taught to take commands from Bhattacharya to assist Jiyon.</p>
                            <p align="justify">Communication was an important aspect. Because Jiyon had a speech problem, and would talk in using single words - say 'water' instead of 'I want water' - Simbaa was trained not to respond to incomplete sentences. "Simbaa doesn't understand human language. But he was taught to follow hand signals that Rohini taught me. For instance, if Jiyon would say, 'ball', I would not react and Simbaa would do nothing. But when Jiyon said, 'get me the ball', I would signal Simbaa to fetch the ball and he would follow my order," says Bhattacharya, explaining how her son was encouraged to speak coherently.</p>
                            <p align="justify">Simbaa was also taught to walk away from Jiyon every time he behaved erratically. "No matter how aggressive Jiyon has been, Simbaa has never retaliated. On one occasion, Jiyon even bit him, but Simbaa simply turned away."</p>
                            <p align="justify"><b>Change is good</b><br /><br />In the last year that Simbaa has become part of the Bhattacharya family, Jiyon has begun to understand that he must speak in complete sentences, and that people get hurt when they are hit. While earlier, he would not sit still in a car, he has now begun to understand the importance of a seat belt, and buckle it on his own. He even requests his mom to play a favourite song on the stereo.</p>
                            <p align="justify">"If, earlier, there would be five bad days a month, now there are only two. I have heard of kids with autism who grow up to be engineers and doctors. But there are others who never learn to communicate. I am open to the possibility that Jiyon will not reach the former level. But the fact that he has started communicating is a huge relief," says Bhattacharya.</p>
                            <p align="justify">Down in the building compound, Jiyon waits for his turn at the swing. "If a child tells him, 'do minute ruk jaa', Jiyon waits. I am no longer worried about my son, only concerned", she adds.</p>
