		                    <center><br /><b>Pet Project</b><br /><i>Priyanka Singh</i><br /></center>
                            <p align="justify"><b>Animals have helped many people overcome the gravest emotional problems. Priyanka Singh tells you about an organisation in the city that has turned this animal-human bond into a professional therapy.</b></p>
                            <p align="center"><img border="0" src="/images/afternoon/2013-09-17/afternoon-2013-09-17-pet-project.jpg" alt="Animal Angels Foundation currently visits special schools and old age homes to help children and senior citizens" align="left" hspace="0" width="540" height="631" /></p>
		                    <p align="justify">Fans and viewers of Aamir Khan's talk show Satyameva Jayate would remember the episode that dealt with child sexual abuse. The instance revolved around LGBT activist Harish Iyer where he spoke about how he survived his traumatic years of child abuse only with the help of his pet dog, Jimmy. This is one of the several instances where animals have helped humans recuperate from emotional, mental and physical hardships that life has thrown at them. This loving power that animals have over us was recognised by clinical psychologists Rohini Fernandes and Radhika Nair, which led them to start Animal Angels Foundation (AAF), under which they conduct Animal Assisted Therapy (AAT).</p>
							<p align="justify"><b>HOW DID AAF START?</b><br />Rohini, a certified practitioner of Animal-Assisted Therapy from the University of North Texas, USA, was struck by the idea of using animals to help people during her fieldwork for Master of Arts degree. where they had to work in the psychiatric wards across various hospitals. She would always think of how it would be easier to connect with clients and get better and quicker responses if she had a dog sitting by her side rather than just apply the conventional therapy that they had to use. She researched this area and decided to combine her knowledge of dogs with her training in the field of psychology to use Animal-Assisted Therapy with her clients. The positive results encouraged her to specialise in this area.</p>
							<p align="justify">About six months after she started Animal Angels Foundation in 2004, she met Radhika, who shared her passion for animals and who also strongly believed in this therapy. They decided to work together and reach out to as many people as they could. Rohini has a Golden Retriever called Angel who she calls her co-therapist and inspiration behind the name Animal Angels Foundation.</p>
							<p align="justify"><b>WHAT IS AAT?</b><br />Animal-Assisted Therapy (AAT) is a process where trained therapy animals assist a medical service professional to motivate and help a child or an adult learn or improve various skills including social, verbal, physical and cognitive skills and it also helps to speed up the recovery process. AAT is used to help clients deal with emotional or behavioural problems by using therapy animals as a part of the treatment process. There is a tailor-made program for each client and the whole process is documented and evaluated. AAT can be integrated into individual or group therapy, across a wide range of age groups and persons with varying disabilities such as learning and developmental disabilities, psychiatric disorders as well as emotional and behavioural problems.</p>
							<p align="justify">AAT is always used in conjunction with other therapies. At Animal Angels Foundation, each of their clients goes through a pre-therapy, mid-therapy and post-therapy evaluation. All therapy sessions are documented through quantitative and qualitative data as well as video recording which is done during the first and last sessions to observe the improvement in the client. Radhika says, "It is a complementary therapy which can be coupled with any other treatment aimed at physical, mental or emotional well-being." Radhika also says that animal therapy works the fastest with children.</p>
							<p align="justify"><b>GET IN TOUCH WITH THEM</b><br />AAF doesn't have its own centre currently. They visit schools and NGOs that ask for their help. Currently they visit eight special schools, two mental health centres and one centre for senior citizens with dementia. While they try to source funding for most group therapies that they undertake at NGOs and organisations affiliated with underprivileged society, they conduct individual sessions too and price it according to your problems and financial conditions. For individual sessions, you need to enroll yourself with Dignity Foundation first and then visit their centres where they conduct group therapies. AAF also trains dogs and cats to be co-therapists and you can get in touch with them if you want your dog or cat to get trained in the same.</p>
